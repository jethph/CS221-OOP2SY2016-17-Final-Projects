    private void rdoGetRAActionPerformed(java.awt.event.ActionEvent evt) {                                         
        if(rdoGetRA.isSelected())
        {
            rdoGetRP.setEnabled(false);
            try
            {
                temp = txtInputUL.getText();
                temp2 = txtInputUW.getText();
                length = Double.parseDouble(temp);
                width = Double.parseDouble(temp2);
            }
            catch(NumberFormatException errors)
            {
                JOptionPane.showMessageDialog(null, "Enter the correct input!");   
            }
            finally
            {
                area = length * width;
                txtGetResultB.setText("Area: " + area);
            }
            
        }
    }                                        

    private void rdoGetRPActionPerformed(java.awt.event.ActionEvent evt) {                                         
        if(rdoGetRP.isSelected())
        {
            rdoGetRA.setEnabled(false);
            try
            {
                temp = txtInputUL.getText();
                temp2 = txtInputUW.getText();
                length = Double.parseDouble(temp);
                width = Double.parseDouble(temp2);
            }
            catch(NumberFormatException errors)
            {
                JOptionPane.showMessageDialog(null, "Enter the correct input!");   
            }
            finally
            {
                perimeter = (2.0 * length) + (2.0 * width);
                txtGetResultB.setText("Perimeter: " + perimeter);
            }
            
        }
    }                                        

