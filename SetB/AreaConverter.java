    private void rdoGetACActionPerformed(java.awt.event.ActionEvent evt) {                                         
        if(rdoGetAC.isSelected())
        {
            rdoGetPC.setEnabled(false);
            try
            {
                temp = txtInputCR.getText();
                radius = Double.parseDouble(temp);
            }
            catch(NumberFormatException errors)
            {
                JOptionPane.showMessageDialog(null, "Enter the correct input!");   
            }
            finally
            {
                area = pi * (radius * radius);
                txtGetResultA.setText("Area: " + area);
            }
        }
    }                                        

    private void rdoGetPCActionPerformed(java.awt.event.ActionEvent evt) {                                         
        if(rdoGetPC.isSelected())
        {
            rdoGetAC.setEnabled(false);
            try
            {
                temp = txtInputCR.getText();
                radius = Double.parseDouble(temp);
            }
            catch(NumberFormatException errors)
            {
                JOptionPane.showMessageDialog(null, "Enter the correct input!");   
            }
            finally
            {
                perimeter = 2.0 * (pi * radius);
                txtGetResultA.setText("Circumference: " + perimeter);
            }
            
        }   
    }                                        

